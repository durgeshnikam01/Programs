let marks = 85; // You can change this value to test

if (marks >= 90) {
    console.log("Excellent");
} else if (marks >= 75) {
    console.log("Very Good");
} else if (marks >= 60) {
    console.log("Good");
} else if (marks >= 45) {
    console.log("Average");
} else if (marks >= 30) {
    console.log("Pass");
} else {
    console.log("Fail");
}